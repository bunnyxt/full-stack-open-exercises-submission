const typeDefs = gql`
  type Author {
    name: String!
    id: ID!
    born: Int
  }

  type Book {
    title: String!
    published: Int!
    author: Author!
    genres: [String!]!
    id: ID!
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(
      author: String
      genre: String
    ): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      published: Int!
      author: String!
      genres: [String!]!
    ): Book
    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
  }
`

const resolvers = {
  Query: {
    bookCount: () => Book.collection.countDocuments(),
    authorCount: () => Author.collection.countDocuments(),
    allBooks: async (root, args) => {
      let books = await Book.find({}).populate('author')
      if (args.author) {
        books = books.filter(book => book.author.name === args.author)
      }
      if (args.genre) {
        books = books.filter(book => book.genres.includes(args.genre))
      }
      return books
    },
    allAuthors: () => Author.find({})
  },
  Mutation: {
    addBook: async (root, args) => {
      if (args.title.length < 3) {
        throw new UserInputError(`book title ${args.title} should contains at lest 3 characters`)
      }
      if (args.author.length < 3) {
        throw new UserInputError(`author name ${args.author} should contains at lest 3 characters`)
      }
      const book = new Book({ ...args })
      let author = await Author.findOne({ name: args.author })
      if (!author) {
        author = new Author({ name: args.author })
        await author.save()
      }
      book.author = author
      return book.save()
    },
    editAuthor: async (root, args) => {
      const author = await Author.findOne({ name: args.name })
      if (!author) {
        throw new UserInputError(`author ${args.name} not found`)
      }
      author.born = args.setBornTo
      await author.save()
      // author.bookCount = await Book.collection.countDocuments({ author })
      return author
    },
  },
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`)
})